#!/usr/bin/env node

const fs = require('fs');
const os = require('os');
const path = require('path');
const { execFileSync } = require('child_process');

const repoRoot = path.resolve(__dirname, '..');
const sourceFiles = ['background.js', 'newtab.js', 'options.js'];
const outputMapPath = path.join(repoRoot, 'assets', 'remixicon', 'lumno-remix-icon-map.json');
const outputRuntimePath = path.join(repoRoot, 'assets', 'remixicon', 'lumno-remix-icon-runtime.js');
const iconPattern = /getRiSvg\(\s*['"]([^'"]+)['"]/g;

function detectIconIds() {
  const ids = new Set();
  sourceFiles.forEach((relativePath) => {
    const absolutePath = path.join(repoRoot, relativePath);
    const contents = fs.readFileSync(absolutePath, 'utf8');
    let match = null;
    while ((match = iconPattern.exec(contents))) {
      ids.add(match[1]);
    }
    iconPattern.lastIndex = 0;
  });
  return Array.from(ids).sort();
}

function resolveRemixVersion() {
  const versionArg = process.argv.find((entry) => entry.startsWith('--version='));
  if (versionArg) {
    return versionArg.slice('--version='.length).trim();
  }
  return execFileSync('npm', ['view', 'remixicon', 'version'], {
    cwd: repoRoot,
    encoding: 'utf8'
  }).trim();
}

function downloadRemixPackage(version) {
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'lumno-remixicon-'));
  const tarballName = execFileSync('npm', ['pack', `remixicon@${version}`, '--silent'], {
    cwd: tempRoot,
    encoding: 'utf8'
  }).trim();
  execFileSync('tar', ['-xf', tarballName], { cwd: tempRoot });
  return {
    cleanup() {
      fs.rmSync(tempRoot, { recursive: true, force: true });
    },
    packageRoot: path.join(tempRoot, 'package')
  };
}

function findIconSvg(packageRoot, iconId) {
  const fileName = `${String(iconId || '').replace(/^ri-/, '')}.svg`;
  const iconsRoot = path.join(packageRoot, 'icons');
  const stack = [iconsRoot];
  while (stack.length) {
    const current = stack.pop();
    const entries = fs.readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      const absolutePath = path.join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(absolutePath);
        continue;
      }
      if (entry.isFile() && entry.name === fileName) {
        return absolutePath;
      }
    }
  }
  return '';
}

function normalizeSvgMarkup(svgSource) {
  let svg = String(svgSource || '')
    .replace(/^\uFEFF/, '')
    .replace(/<\?xml[\s\S]*?\?>/gi, '')
    .replace(/<!DOCTYPE[\s\S]*?>/gi, '')
    .trim();
  svg = svg.replace(/>\s+</g, '><').replace(/\s{2,}/g, ' ');
  svg = svg.replace(/<svg\b([^>]*)>/i, (match, attrs) => {
    let nextAttrs = String(attrs || '')
      .replace(/\s(width|height|class|style|aria-hidden|focusable)=["'][^"']*["']/gi, '')
      .trim();
    if (!/\sxmlns=/.test(nextAttrs)) {
      nextAttrs = `${nextAttrs} xmlns="http://www.w3.org/2000/svg"`.trim();
    }
    if (!/\sviewBox=/.test(nextAttrs)) {
      nextAttrs = `${nextAttrs} viewBox="0 0 24 24"`.trim();
    }
    return `<svg ${nextAttrs} width="100%" height="100%" aria-hidden="true" focusable="false" style="display:block">`;
  });
  return svg;
}

function buildRuntimeSource(version, icons) {
  const iconJson = JSON.stringify(icons, null, 2);
  return `/* eslint-disable */
// Generated by scripts/sync-remix-icon-svg-runtime.js from remixicon@${version}
(function initLumnoRemixIconRuntime(global) {
  const ICONS = ${iconJson};

  function normalizeClassTokens(value) {
    return String(value || '')
      .split(/\\s+/)
      .map((token) => token.trim())
      .filter(Boolean);
  }

  function escapeAttribute(value) {
    return String(value || '').replace(/&/g, '&amp;').replace(/"/g, '&quot;');
  }

  function resolveSizeClass(sizeClass) {
    const normalized = String(sizeClass || '').trim();
    return normalized || 'ri-size-16';
  }

  function resolveSizeValue(sizeClass) {
    const match = resolveSizeClass(sizeClass).match(/ri-size-(\\d+)/);
    return match ? \`\${match[1]}px\` : '16px';
  }

  function getIconMarkup(id, sizeClass, extraClass) {
    const svg = ICONS[id];
    if (!svg) {
      return '';
    }
    const className = ['ri-icon', 'x-lumno-ri-svg']
      .concat(normalizeClassTokens(resolveSizeClass(sizeClass)))
      .concat(normalizeClassTokens(extraClass))
      .join(' ');
    return \`<span class="\${className}" aria-hidden="true" data-ri-icon="\${escapeAttribute(id)}" style="--ri-size: \${resolveSizeValue(sizeClass)};">\${svg}</span>\`;
  }

  global.LumnoRemixIconRuntime = {
    version: ${JSON.stringify(version)},
    icons: ICONS,
    getIconMarkup: getIconMarkup
  };
})(globalThis);
`;
}

function main() {
  const iconIds = detectIconIds();
  if (!iconIds.length) {
    throw new Error('No Remix icon ids were detected from getRiSvg() call sites.');
  }

  const version = resolveRemixVersion();
  const packageHandle = downloadRemixPackage(version);
  const iconMap = {};
  try {
    iconIds.forEach((iconId) => {
      const svgPath = findIconSvg(packageHandle.packageRoot, iconId);
      if (!svgPath) {
        throw new Error(`Unable to find upstream SVG for ${iconId} in remixicon@${version}.`);
      }
      iconMap[iconId] = normalizeSvgMarkup(fs.readFileSync(svgPath, 'utf8'));
    });
  } finally {
    packageHandle.cleanup();
  }

  const mapPayload = {
    generatedAt: new Date().toISOString(),
    sourcePackage: `remixicon@${version}`,
    icons: iconMap
  };

  fs.writeFileSync(outputMapPath, `${JSON.stringify(mapPayload, null, 2)}\n`, 'utf8');
  fs.writeFileSync(outputRuntimePath, buildRuntimeSource(version, iconMap), 'utf8');

  console.log(`Synced ${iconIds.length} Remix icons from remixicon@${version}.`);
  console.log(`- ${path.relative(repoRoot, outputMapPath)}`);
  console.log(`- ${path.relative(repoRoot, outputRuntimePath)}`);
}

main();
